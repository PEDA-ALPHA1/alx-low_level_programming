#include <stdio.h>
int main (void)
{
  int a = 21;
  int b = 20;
  int c ;

  c = a + b;
  printf("Line 1 - Value of c is %\n", c);

  c = a - b;
  printf("Line 2 - Value of c is %\n", c);

  c = a * b;
  printf("Line 3 - Value of c is %\n", c);

  c = a++;
  printf("Line 4 - Value of c is %\n", c);

c = b--;
printf("Line 5 - Value of c is %\n", c);
return (0);
}



























































	 
